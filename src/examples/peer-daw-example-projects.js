// Original educational example sets for operating the V11 Peer DAW UI.
// These are not note-for-note transcriptions and contain no samples, lyrics, or copied recordings.

const MIDI = 'midi';
const CONTROL = 'control';
const CLOCK = 'clock';

const route = (fromModuleId, outputId, toModuleId, inputId) => ({
  from: { moduleId: fromModuleId, outputId },
  to: { moduleId: toModuleId, inputId },
});

const note = (id, beat, noteName, velocity = 0.78, duration = 0.22) => ({
  id,
  kind: MIDI,
  type: 'note-on',
  beat,
  note: noteName,
  velocity,
  duration,
});

const control = (id, beat, target, value) => ({
  id,
  kind: CONTROL,
  type: 'param',
  beat,
  target,
  value,
  duration: 0,
});

function project({ id, title, description, bpm, modules, routes }) {
  return Object.freeze({
    id,
    title,
    description,
    schemaVersion: 1,
    type: 'v11.peer-daw.project',
    exportMode: 'just-project',
    exportedAt: new Date(0).toISOString(),
    modules: Object.freeze(modules),
    routes: Object.freeze(routes),
    assets: Object.freeze([]),
    notes: Object.freeze([
      'Original educational example for learning V11 Peer DAW routing, import/export, and live UI controls.',
      'No Slum Village recordings, samples, lyrics, or melody transcriptions are included.',
      `Set the clock to ${bpm} BPM, press BOOT AUDIO, then START CLOCK to audition the patch.`,
    ]),
  });
}

