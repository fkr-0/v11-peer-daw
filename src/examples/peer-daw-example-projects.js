export function findPeerDawExampleProject(id) {
  return peerDawExampleProjects.find((example) => example.id === id) || null;
}

export function clonePeerDawExampleProject(id) {
  const example = findPeerDawExampleProject(id);
  return example ? JSON.parse(JSON.stringify(example)) : null;
}
